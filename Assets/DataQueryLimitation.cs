/// <summary>
/// Represents the limitations applied to the results of the query.
/// </summary>
public class DataQueryLimitation
{
    /// <summary>
    /// Represents the number of lines to take from the results.
    /// </summary>
    public int linesTaken = 999;

    /// <summary>
    /// Represents the number of lines to skip from the results.
    /// </summary>
    public int linesSkiped = 0;
}